       private void affichertableau(){
            try {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/producttbl", "root", "");
    String query = "SELECT * FROM employee";
    Statement stmt = conn.createStatement();
    ResultSet rs = stmt.executeQuery(query);

    // Set the ResultSet to the table
    DefaultTableModel model = (DefaultTableModel) tableEmployer.getModel();
    model.setRowCount(0); // Clear the table
    while (rs.next()) {
        String name = rs.getString("name");
        int age = rs.getInt("age");
        String gender = rs.getString("gender");
        String job = rs.getString("job");
        double salary = rs.getDouble("salary");
        String phone = rs.getString("phone");
        String email = rs.getString("email");
        String carteIdentite = rs.getString("carteN");
        model.addRow(new Object[]{name, age, gender, job, salary, phone, email, carteIdentite});
    }

    stmt.close();
    conn.close();
} catch (SQLException e) {
    e.printStackTrace();
}
       }
