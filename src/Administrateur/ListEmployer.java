    private void btnsupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsupprimerActionPerformed
        int selectedRow = tableEmployer.getSelectedRow();
    if (selectedRow != -1) {
        String nomEmploye = (String) tableEmployer.getValueAt(selectedRow, 0);
        int confirmation = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment supprimer l'employé " + nomEmploye + " ?", "Confirmation de suppression", JOptionPane.YES_NO_OPTION);
        if (confirmation == JOptionPane.YES_OPTION) {
            try {
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/producttbl", "root", "");
                String sql = "DELETE FROM employee WHERE name = ?";
                PreparedStatement statement = conn.prepareStatement(sql);
                statement.setString(1, nomEmploye);
                int rowsDeleted = statement.executeUpdate();
                if (rowsDeleted > 0) {
                    JOptionPane.showMessageDialog(null, "Employé supprimé avec succès");
                    affichertableau();
                }
                conn.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    } else {
        JOptionPane.showMessageDialog(null, "Veuillez sélectionner un employé à supprimer", "Erreur de sélection", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnsupprimerActionPerformed

