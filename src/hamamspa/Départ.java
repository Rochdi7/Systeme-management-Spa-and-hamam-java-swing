    private void btnbouhdhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbouhdhaActionPerformed
     try {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/producttbl", "root", "");
    Statement statement = conn.createStatement();

    
    ResultSet resultSetRoom = statement.executeQuery("SELECT * FROM room WHERE ava = 'Occupée'");
    if (resultSetRoom.next()) {
        String roomNumber = resultSetRoom.getString("room");
        statement.executeUpdate("UPDATE room SET ava = 'Disponible' WHERE room = '" + roomNumber + "'");
        lblNumero.setText(roomNumber);
    }

    
    ResultSet resultSet = statement.executeQuery("SELECT * FROM reservations");
    while (resultSet.next()) {
        String id = resultSet.getString("id");
        comboidClient.addItem(id); 
    }


    String customerId = (String) comboidClient.getSelectedItem();
    String deleteSql = "DELETE FROM reservations WHERE id = ?";
    PreparedStatement deleteSt = conn.prepareStatement(deleteSql);
    deleteSt.setString(1, customerId); 
    deleteSt.executeUpdate();
    deleteSt.close();

    ImageIcon icon = new ImageIcon("C:\\Users\\rochdi\\Documents\\NetBeansProjects\\HamamSpa\\src\\images\\imgHamam\\check-mark.png");
    JOptionPane.showMessageDialog(this, "Mettra A jour avec Success! , chambre Disponible Maintenant", "Success", JOptionPane.INFORMATION_MESSAGE, icon);
    
    populateComboBox();
    statement.close();
    conn.close();
} catch (SQLException e) {
    e.printStackTrace();
}


    }//GEN-LAST:event_btnbouhdhaActionPerformed

