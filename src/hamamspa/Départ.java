    private void populateComboBox() {
    comboidClient.removeAllItems(); 

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/producttbl", "root", "");
        Statement statement = conn.createStatement();
        ResultSet resultSet = statement.executeQuery("SELECT * FROM reservations");
        while (resultSet.next()) {
            String id = resultSet.getString("id");
            comboidClient.addItem(id); 
        }
        statement.close();
        conn.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }
}
    
