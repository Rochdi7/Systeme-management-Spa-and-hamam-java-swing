    private void btnVerifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifierActionPerformed
       String id = (String) comboID.getSelectedItem();
    String q = "SELECT * FROM room WHERE room = '" + id + "'";
    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/producttbl", "root", "");
        Statement st = conn.createStatement();

        ResultSet resultSet = st.executeQuery(q);
        while (resultSet.next()) {
            txtNchambre.setText(resultSet.getString("room"));
            txtdisbonsibilite.setText(resultSet.getString("ava"));
            txtstatus.setText(resultSet.getString("status"));
        }

        conn.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnVerifierActionPerformed

