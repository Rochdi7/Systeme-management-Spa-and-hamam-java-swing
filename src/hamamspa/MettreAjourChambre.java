    private void populateComboBox() {
        comboID.removeAllItems(); // Clear existing items

        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/producttbl", "root", "");
            Statement statement = conn.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT * FROM room");
            while (resultSet.next()) {
               id = resultSet.getString("room");
                comboID.addItem(id); 
            }
            statement.close();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    
    
    private void btnmettreajourActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmettreajourActionPerformed
        if (evt.getSource() == btnmettreajour) {
             try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/producttbl", "root", "");
        String status = txtstatus.getText();
        String roomNumber = txtNchambre.getText();
        String disponsibilite = txtdisbonsibilite.getText();
        
        String query = "UPDATE room SET status = ?, ava = ? WHERE room = ?";
        PreparedStatement preparedStmt = conn.prepareStatement(query);
        preparedStmt.setString(1, status);
        preparedStmt.setString(2, disponsibilite);
        preparedStmt.setString(3, roomNumber);

        int rowsUpdated = preparedStmt.executeUpdate();
        if (rowsUpdated > 0) {
            ImageIcon icon = new ImageIcon("C:\\Users\\rochdi\\Documents\\NetBeansProjects\\HamamSpa\\src\\images\\imgHamam\\check-mark.png");
JOptionPane.showMessageDialog(this, "Mettre a jour Chambre succéss!", "Success", JOptionPane.INFORMATION_MESSAGE, icon);
        } else {
            JOptionPane.showMessageDialog(null, "Echec Mettre a jour Chambre !");
        }
        
        conn.close();
                }catch (Exception E){
                    E.printStackTrace();
                
            }
        }
    }//GEN-LAST:event_btnmettreajourActionPerformed

