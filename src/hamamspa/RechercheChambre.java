    private void afficherchambre(){
          try {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/producttbl", "root", "");
    String query = "SELECT * FROM room";
    Statement stmt = conn.createStatement();
    ResultSet rs = stmt.executeQuery(query);


    DefaultTableModel model = (DefaultTableModel) tableChambre.getModel();
    model.setRowCount(0); 
    while (rs.next()) {
        int room = rs.getInt("room");
        String disponsibilite = rs.getString("ava");
        String category = rs.getString("status");
       
        model.addRow(new Object[]{room, disponsibilite, category});
    }

    stmt.close();
    conn.close();
} catch (SQLException e) {
    e.printStackTrace();
}

    }
