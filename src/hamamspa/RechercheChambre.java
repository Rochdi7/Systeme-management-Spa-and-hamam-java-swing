    private void btnrechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnrechercherActionPerformed
       String Q = "select * from Room where status = '" + combotypeChambre.getSelectedItem() + "'";
String Q1 = "select * from Room where ava = 'Disponible' And status = '" + combotypeChambre.getSelectedItem() + "'";
try {
    conn c = new conn();
    java.sql.ResultSet resultSet;
    if (checkboxAvailable.isSelected()) {
        resultSet = c.statement.executeQuery(Q1);
    } else {
        resultSet = c.statement.executeQuery(Q);
    }
    
    // Create a DefaultTableModel
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Room");
    model.addColumn("ava");
    model.addColumn("status");
    
    while (resultSet.next()) {
        int room = resultSet.getInt("room");
        String disponsibilite = resultSet.getString("ava");
        String category = resultSet.getString("status");
        model.addRow(new Object[]{room, disponsibilite, category});
    }
    
    tableChambre.setModel(model);
    
    resultSet.close();
    c.statement.close();
    c.connection.close();
} catch (Exception E) {
    E.printStackTrace();
}



    }//GEN-LAST:event_btnrechercherActionPerformed

